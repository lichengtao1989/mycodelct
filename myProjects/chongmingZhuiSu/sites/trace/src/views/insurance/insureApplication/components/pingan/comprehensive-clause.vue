<p>（五）因食品召回而额外租用仓库或存放地而发生的仓储费用；</p>
<p>（六）对召回保险食品进行销毁或其他合理处置的费用；</p>
<p>（七）将召回保险食品的替代食品运回销售者、消费者处所发生的运输费用；</p>
