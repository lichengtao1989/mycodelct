<h3><center>中国平安财产保险股份有限公司<br/>平安附加危机应对费用保险条款</center></h3>

<h4><center>总则</center></h4>
<p><label>第一条</label>  本附加保险合同须附加于平安食品安全责任保险合同（以下简称“主保险合同”）。主保险合同所附条款、投保单、保险单、保险凭证以及批单等，凡与本附加保险合同相关者，均为本附加保险合同的构成部分。凡涉及本附加保险合同的约定，均应采用书面形式。</p>
<p>若主保险合同与本附加保险合同的条款互有冲突，则以本附加保险合同的条款为准。本附加保险合同未尽事宜，以主保险合同的条款规定为准。</p>
<p><label>第二条</label>  本附加保险合同所称“危机应对事件”是指因保险单中列明的食品（以下简称“保险食品”）存在缺陷，已造成或可能造成第三者人身伤亡或财产损失，或保险食品受到他人恶意投毒、勒索、污蔑，并经公安机关立案侦查认定的。</p>
<p><label>第三条</label>  本附加保险合同所称“危机应对费用”指因在保险单中列明的保险期间和承保区域内发生的下列合理、必要的费用：  </p>
<p>（一）因危机应对事件而在报纸、杂志、电台和电视等平面、网络媒体上发布声明、进行澄清等通告费用；</p>
<p>（二）因危机应对事件而向经保险人指定的或事前书面明确同意的危机管理公司或顾问公司进行的危机应对咨询而产生的服务费用；</p>
<p>（三）因危机应对事件而产生的保险食品的检验、检测费用；</p>
<p>（四）因危机应对事件而发生的正常工作人员的加班费用；</p>
<p>（五）因危机应对事件而雇用正常工作人员以外的人员产生的人工费用；</p>
<p>（六）因危机应对事件而发生的文印、邮寄、材料费用；</p>
<p>（七）因危机应对事件而发生的，经公安机关立案侦查认定的赎金等合理应对费用；</p>
<p>（八）投保人申请，经保险人书面同意承保，并在保险单中明确列明的其他费用。</p>
<p>本保险合同所称“食品”指经过加工、制作，用于销售的有形食品，不包括任何形式的服务。</p>

<h4><center>保险责任</center></h4>
<P><label>第四条</label>  兹经保险合同双方同意，保险人对被保险人在保险期间及承保区域内，因保险食品发生本附加险保险合同范围内的危机应对事件而产生的合理、必要的危机应对费用承担赔偿责任，但须满足以下所有条件：</P>
<p>（一）保险人对于危机应对费用所承担的赔偿责任仅以被保险人知道或应当知道危机应对事件发生之日起三十日内或保单约定的最大赔偿期内发生的费用为限；</p>
<p>（二）被保险人需选择经保险人指定的或事前书面明确同意的危机管理公司或顾问公司处理危机应对事件；</p>
<p>（三）投保人应在危机应对费用发生后的三十日内以书面形式向保险人提出索赔申请。</p>

<h4><center>责任除外</center></h4>
<p class="bold"><label>第五条</label>  出现下列任一情形时，保险人不负责赔偿：</p>
<p class="bold">（一）被保险人未取得或被吊销食品生产、销售或餐饮服务经营许可证从事食品生产销售经营活动的；</p>
<p class="bold">（二）被保险人超越经营范围生产、销售或提供食品；</p>
<p class="bold">（三）被保险人在本保险合同列明的经营场所外生产、销售或提供食品；</p>
<p class="bold">（四）被保险人违规使用未经国家有关部门批准使用或国家明令禁用的食品原料或非食用性原料、食品添加剂、营养强化剂或包装材料等来生产、销售或提供食品；</p>
<p class="bold">（五）被保险人超量使用经国家有关部门批准使用的食品原料、食品添加剂、营养强化剂或包装材料等来生产、销售或提供食品。</p>
<p class="bold"><label>第六条</label>  下列原因造成的费用，保险人不负责赔偿：</p>
<p class="bold">（一）投保人、被保险人或其代表的故意行为或重大过失；</p>
<p class="bold">（二）大气污染、土地污染、水污染、放射性污染及其他各种污染；</p>
<p class="bold">（三）食品超过规定的保质期限；</p>
<p class="bold">（四）专供婴幼儿的主、副食品不符合国务院卫生行政部门制定的营养标准；</p>
<p class="bold">（五）被保险人违反《中华人民共和国食品安全法》的规定，雇佣患有痢疾、伤寒、病毒性肝炎等消化道传染病（包括病原携带者），活动性肺结核、化脓性或者渗出性皮肤病以及其他有碍食品卫生的疾病的人员，参加接触直接入口食品的工作；</p>
<p class="bold">（六）因基因或转基因食品所引起的危机应对费用；</p>
<p class="bold">（七）因传染性海绵状脑病，如疯牛病等引起的危机应对费用。</p>
<p class="bold"><label>第七条</label>  下列费用，保险人不负责赔偿：</p>
<p class="bold">（一）罚款、罚金及惩罚性赔偿；</p>
<p class="bold">（二）因食品召回发生的费用；</p>
<p class="bold">（三）违法违规开展公关活动产生的费用；</p>
<p class="bold">（四）未发生本附加保险合同所称“危机应对事件”时，被保险人支付的一切危机应对培训、咨询、策划费用；</p>
<p class="bold">（五）未经公安机关立案侦查认定的恶意投毒、勒索事件，产生的赎金等危机应对费用；</p>
<p class="bold">（六）保险食品本身的损失、退货的货款损失和替换食品的价款损失；</p>
<p class="bold">（七）本保险合同中载明的免赔额，以及按照本保险合同中载明的免赔率计算的免赔额。</p>
<p class="bold"><label>第八条</label>  其他不属于本保险合同责任范围内的费用，保险人不负责赔偿。</p>


