<p><label>第六条</label>  保险事故发生后，被保险人因发生食品召回责任保险事故而被提起仲裁或者诉讼的，对应由被保险人支付的仲裁或者诉讼费用以及事先经保险人书面同意支付的其他必要的、合理的费用（以下简称“法律费用”），保险人按照本附加保险合同约定负责赔偿。</p>

