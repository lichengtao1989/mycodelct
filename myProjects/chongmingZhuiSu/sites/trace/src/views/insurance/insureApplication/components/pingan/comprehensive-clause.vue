<p class="bold">（六）本保险期间起始日之前被保险人就已获悉可能发生的食品召回； </p>
<p class="bold">（七）由于不具备应当具备的功效，或不符合在食品或其包装上注明采用的食品标准，或不符合以食品说明、实物样品等方式表明的质量状况而导致的食品召回；</p>
<p class="bold">（八）因基因或转基因食品所引起的召回；</p>
<p class="bold">（九）因传染性海绵状脑病，如疯牛病等导致的被保险食品的召回；</p>
<p class="bold">（十）单独因包装不合格或包装标示错误引起的食品召回。</p>
<p class="bold"><label>第九条</label>  下列损失、费用和责任，保险人不负责赔偿：</p>
<p class="bold">（一）被召回保险食品本身的损失、退货的货款损失和替换食品的价款损失；</p>
<p class="bold">（二）对召回开始实施之日已超过生产商标示的保质期的保险食品或以保险食品为原、配料的其他食品实施召回的费用或责任；</p> 
<p class="bold">（三）对定制食品、试验品实施召回的费用或责任；</p>
<p class="bold">（四）因保险食品或以保险食品为原、配料的其他食品在使用或消费过程中造成人身伤亡或其他财产损失，应当由被保险人承担的违约责任、侵权责任等一切法律责任；</p>
<p class="bold">（五）罚款、罚金及惩罚性赔偿；</p>
<p class="bold">（六）精神损害赔偿；</p>
<p class="bold">（七）间接损失；</p>
<p class="bold">（八）销售到境外（包括港、澳、台）的食品所引起的召回费用；</p>
<p class="bold">（九）本附加保险合同中载明的免赔额，以及按照本附加保险合同中载明的免赔率计算的免赔额。
第十条  其他不属于本附加保险合同责任范围内的损失和费用，保险人不负责赔偿。</p>

<h4><center>责任限额与免赔约定</center></h4>
<p><label>第十一条</label>  本附加保险合同的责任限额包括每次事故责任限额、累计责任限额，由投保人与保险人在签订本附加保险合同时协商确定，并载于本附加保险合同中。</p>
<p>每次事故免赔额和免赔率由投保人与保险人在签订本附加保险合同时协商确定，并在本附加保险合同中载明。</p>

<h4><center>保险期间与赔偿期</center></h4>
<p><label>第十二条</label>  除另有约定外，保险期间为一年，以保险单载明的起讫时间为准。</p>
<p><label>第十三条  </label>赔偿期指自召回开始实施之日起，至召回活动结束之日止的期间，但最长不超过保险单中约定的最大赔偿期。</p>
<p>被保险人自愿实施召回的，召回开始实施之日为被保险人首次向公众发布召回公告之日，或被保险人首次向保险人提交关于该次召回的书面报告之日，以较早发生者为准；政府部门指令实施召回的，召回开始实施之日为政府部门发布召回指令之日。</p>
<p>召回活动结束之日为前款公告或指令中指定的结束日期，或待召回的所有保险食品完成召回之日，以较早发生者为准。</p>
<p>由于保险食品的同一缺陷导致的对保险食品、其他食品的一切食品召回，包括不同批次食品的召回，视为一次召回事件，在本附加保险合同下视为一次事故。</p>
<p>最大赔偿期由投保人与保险人在签订本附加保险合同时协商确定，并载于本附加保险合同中。</p>
